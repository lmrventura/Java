    private void lblImage() {
        int numero = Integer.parseInt(txtNum.getText());
        if (numero == 13){
            imgCandidato.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/lula.jpg")));
        }else if (numero == 17){
            imgCandidato.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/bolsonaro.jpeg")));
        }else if (numero == 10){
            imgCandidato.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/moro.jpg")));
            //imgCandidato.setVisible(true);
        }else if (numero == 0){
            imgCandidato.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/branco.png")));
        }else if (numero == 01){
            imgCandidato.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/nulo.jpg")));
        }
    }   
    
